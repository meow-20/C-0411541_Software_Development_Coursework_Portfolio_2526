# Integer
age = 20
print("Age:", age)
print("Type of age:", type(age))

# Float
height = 5.9
print("\nHeight:", height)
print("Type of height:", type(height))

# String
name = "Bansari"
print("\nName:", name)
print("Type of name:", type(name))

# Boolean
is_student = True
print("\nIs Student:", is_student)
print("Type of is_student:", type(is_student))

# Simple operations with different data types
print("\n--- Operations ---")
print("Age after 5 years:", age + 5)            # Integer operation
print("Height in 2 years (example):", height + 0.1)  # Float operation
print("Greeting:", "Hello " + name)             # String concatenation
print("Is age greater than 18?", age > 18)      # Boolean result
